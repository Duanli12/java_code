package com.dl.base;

/*
	常量（程序运行期间，不能改变的量）：
			1.字符串，可以存储0个，1个，多个字符，如"","A","asfsfsf","哈哈"
			2.字符，只能存储一个字符，不能多也不能少，如' ','a'
			3.整数,如235
			4.浮点数,如25.152
			5.布尔类型,true，false
			6.空常量，null，代表空，什么都没有
*/
public class A02ConstantTest {
    public static void main(String[] args) {
        System.out.println("HelloWorld!!2你好");
        System.out.println("");
        System.out.println("我");

        System.out.println("=====================");
        System.out.println('a');
        System.out.println('2');
        System.out.println('^');
        System.out.println(' ');
//        System.out.println('ab');
//        System.out.println('');

        System.out.println("=====================");
        System.out.println(10);
        System.out.println(250);

        System.out.println("=======================");
        System.out.println(1.23);
        System.out.println(100.0);

        System.out.println("=======================");
        System.out.println(true);
        System.out.println(false);

        System.out.println("=======================");
//        System.out.println(null); 空常量不能进行输出

    }

}